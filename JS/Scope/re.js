var firstName; // Undefined
firstName = 'Oscar'; // Asignación
console.log(firstName);

var lastName = 'David'; // Asignando y declarando
lastName = 'Ana'; // Reasignación
console.log(lastName);

var secondName = 'David';
var secondName = 'Ana';
console.log(secondName);

// Let

let fruit = 'Apple'; // Declarar y asignar
fruit = 'Kiwi'; // Reasignar, no se puede redeclarar
console.log(fruit);

// Const
const animal = 'Dog'; // Declarar y asignar, no se puede ni reasignar ni redeclarar
console.log(animal);